export function getClassBySlug(slug: string) {
  return classes.find((item) => item.slug === slug);
}

export function getSkillBySlug(slug: string) {
  return skills.find((item) => item.slug === slug);
}

export function getBuildBySlug(slug: string) {
  return builds.find((item) => item.slug === slug);
}

export function getBuildsForClass(classSlug: string) {
  return builds.filter((build) => build.class === classSlug);
}

export function getBuildForGenerator(classSlug: string, difficulty: Difficulty) {
  return (
    builds.find((build) => build.class === classSlug && build.difficulty === difficulty) ??
    builds.find((build) => build.class === classSlug) ??
    builds.find((build) => build.difficulty === difficulty) ??
    builds[0]
  );
}
