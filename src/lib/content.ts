type SkillSeed = {
  name: string;
  aliases?: string[];
  tags: string[];
  role: string;
  levelRequirement?: number;
};

type BuildSeed = {
  title: string;
  class: string;
  playstyle: string;
  difficulty: Difficulty;
  mainSkill: string;
  supportSkills: string[];
  searchIntent: string;
  goal: string;
  avoid: string;
};

function slugifySkillName(name: string) {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function createExpandedSkill(seed: SkillSeed, id: number): Skill {
  const tagLabel = seed.tags.join(", ");

  return {
    id,
    name: seed.name,
    slug: slugifySkillName(seed.name),
    aliases: seed.aliases,
    tags: seed.tags,
    description: `${seed.name} is a POE2 ${tagLabel} skill option for ${seed.role}.`,
    level_requirement: seed.levelRequirement ?? 1,
    bestFor: `Players who need ${seed.role}.`,
    decisionRule: `Choose it when your build needs ${seed.role}; skip it if a simpler core skill already solves that job.`,
  };
}

function createExpandedBuild(seed: BuildSeed, id: number): Build {
  return {
    id,
    title: seed.title,
    slug: slugifySkillName(seed.title),
    class: seed.class,
    playstyle: seed.playstyle,
    difficulty: seed.difficulty,
    searchIntent: seed.searchIntent,
    promise: `A decision-first ${seed.playstyle.toLowerCase()} route for players who want ${seed.goal}.`,
    content: `${seed.title} is for players deciding whether ${seed.mainSkill} should be their main skill direction. Keep the first version narrow: one main skill, two support tools, and clear defensive priorities before chasing endgame scaling.`,
    coreSkills: [seed.mainSkill, ...seed.supportSkills],
    goodFor: [`Players who want ${seed.goal}`, `${seed.class} players comparing build directions`, "Searchers who need a fast starter decision"],
    notFor: [seed.avoid, "Players who want a full passive tree planner", "Players looking for exact patch-sensitive DPS math"],
    levelingPlan: [
      `Use ${seed.mainSkill} as the main identity before adding extra damage packages.`,
      `Add ${seed.supportSkills[0]} when clear, control, or boss pressure starts to fall behind.`,
      "Upgrade defenses whenever deaths cost more time than low damage does.",
    ],
  };
}

